import math

print("Square root of 16 is", math.sqrt(16))
print("pi is", math.pi)
print("Floor of 2.5 is", math.floor(2.5))
print("Ceil of 2.5 is", math.ceil(2.5))
print("Absolute value of -2.5 is", math.fabs(-2.5))
print("Factorial of 5 is", math.factorial(5))
print("5 to the power of 2 is", math.pow(5, 2))